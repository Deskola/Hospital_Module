     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'subject_name'=>'required',
        ]);

        $sbj = new Subject;
        $sbj->subject_name = $request->input('subject_name');
        $sbj->save();

        return redirect('subject.create');
    }

